// Boolean
// True -> verdadeiro
// False -> falso

const comparacao = 6 === meuNumero;
console.log(comparacao)

const comparacaoII = "6" === meuNumero;
console.log(comparacaoII)

const comparacaoIII = "6" == meuNumero;
